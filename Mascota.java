
package centroRescate;

public class Mascota {

    private String nombre;
    private String raza;
    private String color;
    private int edad;
    private int nivel_de_entrenamiento;
    private String texoplasmosis;


    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getRaza() {
        return raza;
    }

    public void setRaza(String raza) {
        this.raza = raza;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public int getEdad() {
        return edad;
    }

    public void setEdad(int edad) {
        this.edad = edad;
    }

    public int getNivel_de_entrenamiento() {
        return nivel_de_entrenamiento;
    }

    public void setNivel_de_entrenamiento(int nivel_de_entrenamiento) {
        this.nivel_de_entrenamiento = nivel_de_entrenamiento;
    }

    public String getTexoplasmosis() {
        return texoplasmosis;
    }

    public void setTexoplasmosis(String texoplasmosis) {
        this.texoplasmosis = texoplasmosis;
    }
    
    
}
